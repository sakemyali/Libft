#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nb;
	char	ch;

	nb = n;
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr_fd((int)(nb / 10), fd);
	ch = (char)((nb % 10) + '0');
	write(fd, &ch, 1);
}
